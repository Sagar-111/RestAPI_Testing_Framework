package pack1.Payload;

public class UserData {
	int id;
	String user_name;
	String user_email;
	String user_location;
	String user_profilePicture;
	String createdat;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getUser_name() {
		return user_name;
	}

	public void setUser_name(String user_name) {
		this.user_name = user_name;
	}

	public String getUser_email() {
		return user_email;
	}

	public void setUser_email(String user_email) {
		this.user_email = user_email;
	}

	public String getUser_location() {
		return user_location;
	}

	public void setUser_location(String user_location) {
		this.user_location = user_location;
	}

	public String getUser_profilePicture() {
		return user_profilePicture;
	}

	public void setUser_profilePicture(String user_profilePicture) {
		this.user_profilePicture = user_profilePicture;
	}

	public String getCreatedat() {
		return createdat;
	}

	public void setCreatedat(String createdat) {
		this.createdat = createdat;
	}

}
